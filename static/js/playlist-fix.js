const CACHE_DURATION = 30000; // 30 segundos

function getPlaylistIdCached() {
    const now = Date.now();
    
    // Si tenemos cache v√°lido, usarlo
    if (cachedPlaylistId && (now - cacheTime) < CACHE_DURATION) {
        console.log(`üéØ [getPlaylistId] Usando ID cachado: ${cachedPlaylistId}`);
        return cachedPlaylistId;
    }
    
    // Obtener nuevo ID y cachearlo
    const id = getPlaylistId();
    if (id) {
        cachedPlaylistId = id;
        cacheTime = now;
        console.log(`üíæ [getPlaylistId] ID cacheado: ${id}`);
    }
    
    return id;
}

