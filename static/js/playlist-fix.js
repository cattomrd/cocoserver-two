/**
 * Obtener ID de playlist de manera robusta con m√∫ltiples fallbacks
 */
function getPlaylistId() {
    console.log('üîç [getPlaylistId] Iniciando b√∫squeda de ID de playlist...');
    
    try {
        // ==========================================
        // M√âTODO 1: OBTENER DE LA URL (M√ÅS CONFIABLE)
        // ==========================================
        const urlParams = new URLSearchParams(window.location.search);
        const idFromUrl = urlParams.get('id');
        
        if (idFromUrl) {
            const parsedId = parseInt(idFromUrl);
            if (!isNaN(parsedId) && parsedId > 0) {
                console.log(`‚úÖ [getPlaylistId] ID obtenido de URL: ${parsedId}`);
                return parsedId;
            }
        }
        
        // ==========================================
        // M√âTODO 2: EXTRAER DEL PATH DE LA URL
        // ==========================================
        const pathMatch = window.location.pathname.match(/\/playlists?\/(\d+)/);
        if (pathMatch && pathMatch[1]) {
            const parsedId = parseInt(pathMatch[1]);
            if (!isNaN(parsedId) && parsedId > 0) {
                console.log(`‚úÖ [getPlaylistId] ID obtenido del path: ${parsedId}`);
                return parsedId;
            }
        }
        
        // ==========================================
        // M√âTODO 3: ELEMENTO HIDDEN INPUT
        // ==========================================
        const idElement = document.getElementById('playlist-id');
        if (idElement && idElement.value) {
            const parsedId = parseInt(idElement.value);
            if (!isNaN(parsedId) && parsedId > 0) {
                console.log(`‚úÖ [getPlaylistId] ID obtenido de elemento hidden: ${parsedId}`);
                return parsedId;
            }
        }
        
        // ==========================================
        // M√âTODO 4: VARIABLES GLOBALES
        // ==========================================
        if (window.currentPlaylistId) {
            const parsedId = parseInt(window.currentPlaylistId);
            if (!isNaN(parsedId) && parsedId > 0) {
                console.log(`‚úÖ [getPlaylistId] ID obtenido de variable global: ${parsedId}`);
                return parsedId;
            }
        }
        
        if (window.currentPlaylistData && window.currentPlaylistData.id) {
            const parsedId = parseInt(window.currentPlaylistData.id);
            if (!isNaN(parsedId) && parsedId > 0) {
                console.log(`‚úÖ [getPlaylistId] ID obtenido de datos globales: ${parsedId}`);
                return parsedId;
            }
        }
        
        // ==========================================
        // M√âTODO 5: PARSEAR JSON DE MANERA SEGURA
        // ==========================================
        const playlistElement = document.getElementById('playlist-data');
        if (playlistElement && playlistElement.textContent) {
            try {
                console.log('üîç [getPlaylistId] Intentando parsear JSON...');
                
                // Limpiar el JSON antes de parsearlo
                let jsonText = playlistElement.textContent.trim();
                
                // Detectar y corregir el error espec√≠fico: "field": ,
                console.log('üîß [getPlaylistId] Corrigiendo JSON malformado...');
                
                // Patr√≥n para encontrar campos con comas sin valor
                const malformedPattern = /"([^"]+)":\s*,/g;
                let correctedJson = jsonText;
                let corrections = 0;
                
                // Reemplazar campos malformados con null
                correctedJson = correctedJson.replace(malformedPattern, (match, fieldName) => {
                    corrections++;
                    console.log(`üîß [getPlaylistId] Corrigiendo campo malformado: "${fieldName}": , -> "${fieldName}": null`);
                    return `"${fieldName}": null,`;
                });
                
                // Otros patrones comunes de JSON malformado
                correctedJson = correctedJson
                    .replace(/,\s*}/g, '}')  // Eliminar comas antes de cierre
                    .replace(/,\s*]/g, ']')  // Eliminar comas antes de cierre de array
                    .replace(/:\s*,/g, ': null,');  // Campos sin valor
                
                if (corrections > 0) {
                    console.log(`üîß [getPlaylistId] JSON corregido con ${corrections} correcciones`);
                }
                
                // Intentar parsear el JSON corregido
                const templateData = JSON.parse(correctedJson);
                
                if (templateData && templateData.id) {
                    const parsedId = parseInt(templateData.id);
                    if (!isNaN(parsedId) && parsedId > 0) {
                        console.log(`‚úÖ [getPlaylistId] ID obtenido de JSON corregido: ${parsedId}`);
                        
                        // Guardar los datos corregidos para uso futuro
                        window.currentPlaylistData = templateData;
                        if (templateData.videos) {
                            window.playlistVideos = templateData.videos;
                        }
                        
                        return parsedId;
                    }
                }
                
            } catch (jsonError) {
                console.warn('‚ö†Ô∏è [getPlaylistId] Error parseando JSON incluso despu√©s de correcciones:', jsonError.message);
                
                // Intentar extraer ID del JSON malformado usando regex como √∫ltimo recurso
                try {
                    console.log('üîç [getPlaylistId] Intentando extraer ID con regex...');
                    const idMatch = playlistElement.textContent.match(/"id":\s*(\d+)/);
                    if (idMatch && idMatch[1]) {
                        const parsedId = parseInt(idMatch[1]);
                        if (!isNaN(parsedId) && parsedId > 0) {
                            console.log(`‚úÖ [getPlaylistId] ID extra√≠do con regex: ${parsedId}`);
                            return parsedId;
                        }
                    }
                } catch (regexError) {
                    console.warn('‚ö†Ô∏è [getPlaylistId] Error extrayendo ID con regex:', regexError.message);
                }
            }
        }
        
        // ==========================================
        // M√âTODO 6: META TAGS (SI EXISTEN)
        // ==========================================
        const metaPlaylistId = document.querySelector('meta[name="playlist-id"]');
        if (metaPlaylistId && metaPlaylistId.content) {
            const parsedId = parseInt(metaPlaylistId.content);
            if (!isNaN(parsedId) && parsedId > 0) {
                console.log(`‚úÖ [getPlaylistId] ID obtenido de meta tag: ${parsedId}`);
                return parsedId;
            }
        }
        
        // ==========================================
        // M√âTODO 7: DATA ATTRIBUTES
        // ==========================================
        const bodyElement = document.body;
        if (bodyElement && bodyElement.dataset.playlistId) {
            const parsedId = parseInt(bodyElement.dataset.playlistId);
            if (!isNaN(parsedId) && parsedId > 0) {
                console.log(`‚úÖ [getPlaylistId] ID obtenido de data attribute: ${parsedId}`);
                return parsedId;
            }
        }
        
        // ==========================================
        // NO SE ENCONTR√ì ID
        // ==========================================
        console.error('‚ùå [getPlaylistId] No se pudo obtener ID de playlist desde ninguna fuente');
        console.log('üîç [getPlaylistId] Fuentes verificadas: URL, path, elemento hidden, variables globales, JSON, meta tags, data attributes');
        
        return null;
        
    } catch (error) {
        console.error('‚ùå [getPlaylistId] Error general en funci√≥n:', error);
        return null;
    }
}

/**
 * Funci√≥n auxiliar para corregir JSON malformado
 */
function fixMalformedJson(jsonString) {
    if (!jsonString) return jsonString;
    
    try {
        console.log('üîß [fixMalformedJson] Iniciando correcci√≥n de JSON...');
        
        let fixed = jsonString.trim();
        let corrections = 0;
        
        // Lista de correcciones comunes para JSON malformado
        const fixes = [
            // Corregir campos con comas sin valor: "field": ,
            {
                pattern: /"([^"]+)":\s*,/g,
                replacement: '"$1": null,',
                description: 'Campos con valor vac√≠o'
            },
            // Corregir m√∫ltiples comas: ,,
            {
                pattern: /,+/g,
                replacement: ',',
                description: 'M√∫ltiples comas consecutivas'
            },
            // Corregir comas antes de cierre de objeto
            {
                pattern: /,(\s*})/g,
                replacement: '$1',
                description: 'Comas antes de cierre de objeto'
            },
            // Corregir comas antes de cierre de array
            {
                pattern: /,(\s*])/g,
                replacement: '$1',
                description: 'Comas antes de cierre de array'
            },
            // Corregir valores undefined
            {
                pattern: /:\s*undefined/g,
                replacement: ': null',
                description: 'Valores undefined'
            },
            // Corregir comillas simples (JSON requiere comillas dobles)
            {
                pattern: /'([^']*)'/g,
                replacement: '"$1"',
                description: 'Comillas simples'
            }
        ];
        
        // Aplicar cada correcci√≥n
        fixes.forEach(fix => {
            const matches = fixed.match(fix.pattern);
            if (matches) {
                corrections += matches.length;
                fixed = fixed.replace(fix.pattern, fix.replacement);
                console.log(`üîß [fixMalformedJson] Corregido: ${fix.description} (${matches.length} instancias)`);
            }
        });
        
        if (corrections > 0) {
            console.log(`‚úÖ [fixMalformedJson] JSON corregido con ${corrections} correcciones total`);
        } else {
            console.log('‚úÖ [fixMalformedJson] JSON no necesitaba correcciones');
        }
        
        return fixed;
        
    } catch (error) {
        console.error('‚ùå [fixMalformedJson] Error corrigiendo JSON:', error);
        return jsonString; // Devolver original si falla la correcci√≥n
    }
}

